-- Limpiar políticas si ya existían.
drop policy if exists "Lectura publica matches" on public.matches;
drop policy if exists "Lectura publica participants" on public.participants;
drop policy if exists "Alta publica participants" on public.participants;
drop policy if exists "Lectura publica predictions" on public.predictions;
drop policy if exists "Alta pronosticos abiertos" on public.predictions;
drop policy if exists "Editar pronosticos abiertos" on public.predictions;

-- Todos pueden ver partidos, participantes y pronósticos para que funcione el ranking.
create policy "Lectura publica matches"
on public.matches for select
using (true);

create policy "Lectura publica participants"
on public.participants for select
using (true);

create policy "Alta publica participants"
on public.participants for insert
with check (true);

create policy "Lectura publica predictions"
on public.predictions for select
using (true);

-- Se pueden cargar/editar pronósticos solo mientras el partido no esté bloqueado y no tenga resultado real.
create policy "Alta pronosticos abiertos"
on public.predictions for insert
with check (
  exists (
    select 1 from public.matches m
    where m.id = predictions.match_id
      and m.locked = false
      and m.real_a is null
      and m.real_b is null
  )
);

create policy "Editar pronosticos abiertos"
on public.predictions for update
using (
  exists (
    select 1 from public.matches m
    where m.id = predictions.match_id
      and m.locked = false
      and m.real_a is null
      and m.real_b is null
  )
)
with check (
  exists (
    select 1 from public.matches m
    where m.id = predictions.match_id
      and m.locked = false
      and m.real_a is null
      and m.real_b is null
  )
);

