create extension if not exists pgcrypto;

create table if not exists public.matches (
  id bigint generated by default as identity primary key,
  match_no int not null unique,
  round text not null default '16avos',
  team_a text not null,
  team_b text not null,
  starts_at timestamptz,
  real_a int check (real_a is null or real_a >= 0),
  real_b int check (real_b is null or real_b >= 0),
  locked boolean not null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.participants (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  name_key text not null unique,
  created_at timestamptz not null default now()
);

create table if not exists public.predictions (
  id uuid primary key default gen_random_uuid(),
  participant_id uuid not null references public.participants(id) on delete cascade,
  match_id bigint not null references public.matches(id) on delete cascade,
  pred_a int not null check (pred_a >= 0),
  pred_b int not null check (pred_b >= 0),
  updated_at timestamptz not null default now(),
  unique (participant_id, match_id)
);

-- Actualiza updated_at automáticamente cuando cambian los pronósticos.
create or replace function public.set_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

drop trigger if exists trg_predictions_updated_at on public.predictions;
create trigger trg_predictions_updated_at
before update on public.predictions
for each row execute function public.set_updated_at();

